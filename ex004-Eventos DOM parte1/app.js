 function clicar(){
    var a = document.getElementById('area')
    a.innerText = 'clicou!';
    a.style.background = 'red';
}

function entrar(){
    var a = document.getElementById('area')
    a.innerText = 'entrou!';
    a.style.background = 'blue';
}

function sair(){
    var a = document.getElementById('area')
    a.innerText = 'saiu!';
    a.style.background = 'yellow';
    a.style.color = 'black';
}